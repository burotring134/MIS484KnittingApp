// Threadia preset templates — 9 hand-crafted cross-stitch patterns
// organised by difficulty. Each template is parsed at load time from a
// compact ASCII representation: each character is one cell, mapped to a
// colour index via the palette key.

const SYMBOLS = [
  '■','●','▲','★','♦','♥','+','▼','◆','○',
  '□','△','☆','◇','♡','×','❖','◉','✚','✿',
];

function parse(rows, palette) {
  const lines = rows.split('\n').filter((l) => l.length > 0);
  const height = lines.length;
  const width  = Math.max(...lines.map((l) => l.length));

  const grid = lines.map((line) =>
    Array.from({ length: width }).map((_, x) => {
      const ch = line[x] ?? ' ';
      const idx = palette.findIndex((p) => p.ch === ch);
      return idx >= 0 ? idx : 0;
    })
  );

  // count usage per colour
  const counts = palette.map(() => 0);
  for (const row of grid) for (const id of row) counts[id]++;

  const colors = palette.map((p, i) => ({
    id:      i,
    hex:     p.hex,
    dmcCode: p.dmc,
    dmcName: p.name,
    dmcHex:  p.hex,
    symbol:  SYMBOLS[i % SYMBOLS.length],
    count:   counts[i],
  }));

  return { width, height, grid, colors };
}

